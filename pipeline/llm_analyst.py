"""
LLM Analyst Module
──────────────────
Sends salary prediction data to a local Ollama instance (llama3.2) and returns
structured narrative insights.

Two generation modes:
    generate_micro_narrative()  → 2-sentence summary for a single prediction
    generate_global_summary()   → 3-paragraph executive summary for the board

Design philosophy:
    All reasoning (above/below median, percentage diff, status labels) is
    pre-calculated in Python. The LLM receives explicit facts and tone
    instructions so it cannot contradict the data. Temperature is kept at
    0.2 to minimise creative hallucination.

Failure modes handled gracefully:
- Ollama not running            → ConnectionError       → fallback narrative
- Ollama request too slow       → Timeout               → fallback narrative
- LLM returns non-JSON text     → JSONDecodeError       → fallback narrative
- LLM JSON missing 'narrative'  → KeyError              → fallback narrative
- JSON valid but wrong shape    → ValidationError       → fallback narrative
- Any other unexpected error    → Exception             → fallback narrative
"""

