# ── Output schema ──────────────────────────────────────────────────────────────
class LLMResponse(BaseModel):
    """
    The exact JSON structure we expect the LLM to return.
    Pydantic enforces this — any deviation triggers a ValidationError,
    which we catch and replace with the fallback narrative.
    """

    narrative: str


def _extract_narrative(parsed: dict) -> str:
    """
    Pull a narrative string out of the LLM's parsed JSON dict.

    llama3.2 sometimes ignores the requested key name and returns things like
    {"response": "..."} or {"insight": "..."} instead of {"narrative": "..."}.
    This function tries a list of common candidate keys first, then falls back
    to the first string-valued entry it finds anywhere in the dict.

    Raises ValueError if no usable string value can be found at all.
    """
    # 1. Try each known candidate key in priority order
    for key in _CANDIDATE_KEYS:
        value = parsed.get(key)
        if isinstance(value, str) and value.strip():
            if key != "narrative":
                logger.warning("LLM used key '%s' instead of 'narrative' — extracted anyway.", key)
            return value.strip()

    # 2. Fallback: scan all top-level values for the first non-empty string
    for key, value in parsed.items():
        if isinstance(value, str) and value.strip():
            logger.warning("LLM used unexpected key '%s' — extracted first string value.", key)
            return value.strip()

    # 3. Nothing usable found
    raise ValueError(f"No string value found in LLM JSON. Keys present: {list(parsed.keys())}")


def _call_ollama(system_prompt: str, user_message: str, label: str, fallback: str) -> str:
    """
    Shared Ollama call logic for both micro and global generation modes.

    Parameters
    ----------
    system_prompt : str
        The system prompt defining the LLM's role and output rules.
    user_message : str
        The user-facing data prompt with pre-calculated facts.
    label : str
        A short label for logging (e.g. job title or "global_summary").
    fallback : str
        The fallback string to return on any failure.

    Returns
    -------
    str
        The extracted narrative, or fallback on any error.
    """
    request_body = {
        "model": OLLAMA_MODEL,
        "system": system_prompt,
        "prompt": user_message,
        "format": "json",
        "stream": False,
        "options": {
            "temperature": OLLAMA_TEMPERATURE,
        },
    }

    try:
        response = requests.post(OLLAMA_URL, json=request_body, timeout=OLLAMA_TIMEOUT)
        response.raise_for_status()

        raw_text: str = response.json().get("response", "")
        parsed: dict = json.loads(raw_text)
        narrative_text: str = _extract_narrative(parsed)
        validated = LLMResponse(narrative=narrative_text)
        return validated.narrative

    except requests.exceptions.ConnectionError:
        logger.error(
            "Ollama is not running. Could not connect to %s. "
            "Start it with: ollama serve",
            OLLAMA_URL,
        )
        return fallback

    except requests.exceptions.Timeout:
        logger.error(
            "Ollama request timed out after %ds for: %s",
            OLLAMA_TIMEOUT,
            label,
        )
        return fallback

    except requests.exceptions.HTTPError as exc:
        logger.error(
            "Ollama HTTP error %s: %s",
            exc.response.status_code,
            exc.response.text,
        )
        return fallback

    except json.JSONDecodeError as exc:
        logger.error(
            "LLM returned non-JSON text for '%s': %s. Raw: %.200s",
            label,
            exc,
            raw_text if "raw_text" in dir() else "(no response captured)",
        )
        return fallback

    except (KeyError, TypeError) as exc:
        logger.error(
            "Unexpected structure in LLM response for '%s': %s",
            label,
            exc,
        )
        return fallback

    except ValidationError as exc:
        logger.error(
            "LLM JSON failed Pydantic validation for '%s': %s",
            label,
            exc,
        )
        return fallback

    except Exception as exc:
        logger.error(
            "Unexpected error calling Ollama for '%s': %s",
            label,
            exc,
            exc_info=True,
        )
        return fallback


# ── Micro narrative (per-record) ──────────────────────────────────────────────

def generate_micro_narrative(
    payload: dict,
    predicted_salary: float,
    global_medians: dict,
) -> str:
    """
    Calls the local Ollama LLM to generate a 2-sentence micro-narrative
    for a single salary prediction record.

    All reasoning is done in Python before the prompt is sent:
    - percentage_diff and status (ABOVE/BELOW) are pre-calculated
    - the LLM receives explicit tone instructions anchored to the status

    Parameters
    ----------
    payload : dict
        The original input combination sent to the FastAPI.
    predicted_salary : float
        The predicted salary in USD returned by the ML model.
    global_medians : dict
        Reference statistics (e.g. overall_median) for comparison.

    Returns
    -------
    str
        A 2-sentence narrative. Never raises — returns FALLBACK_NARRATIVE
        on any error so the orchestrator loop can always continue.
    """
    job_title = payload.get("job_title", "Unknown")
    experience_level = payload.get("experience_level", "Unknown")
    experience_label = EXP_LABELS.get(experience_level, experience_level)
    company_location = payload.get("company_location", "Unknown")
    country_name = COUNTRY_NAMES.get(company_location, company_location)

    # ── Pre-calculate facts in Python (not left to the LLM) ──────────────
    # Use per-category median when available; fall back to overall median.
    job_category = payload.get("job_category", job_title)
    category_medians = global_medians.get("category_medians", {})
    median = category_medians.get(job_category, global_medians.get("overall_median", 0))
    delta = predicted_salary - median
    percentage_diff = (delta / median * 100) if median else 0

    # ── 5-band granular status ───────────────────────────────────────────
    # ── 5-band granular status (data-driven from EDA percentiles) ──────
    # P10 avg = -64%, P25 avg = -39%, P75 avg = +50%, P90 avg = +96%
    if percentage_diff < -64:
        granular_status = "exceptionally below"
    elif percentage_diff < -39:
        granular_status = "below"
    elif percentage_diff <= 50:
        granular_status = "on par with"
    elif percentage_diff <= 96:
        granular_status = "above"
    else:
        granular_status = "exceptionally above"

    # ── Determine the primary driver via hierarchical rule engine ─────────
    primary_driver = determine_primary_driver(payload, granular_status)

    # ── Build the user message with injected facts ────────────────────────
    user_message = (
        f"JOB: {job_title}\n"
        f"EXPERIENCE: {experience_label}\n"
        f"LOCATION: {country_name}\n"
        f"PREDICTED: ${predicted_salary:,.0f}\n"
        f"MEDIAN: ${median:,.0f}\n"
        f"GRANULAR_STATUS: {granular_status}\n"
    )

    # ── Sentence 1 from LLM, Sentence 2 built deterministically ─────────
    sentence_1 = _call_ollama(MICRO_SYSTEM_PROMPT, user_message, job_title, FALLBACK_NARRATIVE)
    sentence_2 = f"This positioning is primarily driven by {primary_driver}."
    return f"{sentence_1} {sentence_2}"


# ── Global summary (executive-level) ─────────────────────────────────────────

def generate_global_summary(
    overall_median: float,
    top_role: str,
    top_role_median: float,
    high_tier_median: float,
    low_tier_median: float,
    total_records: int,
) -> str:
    """
    Calls the local Ollama LLM to generate a 3-paragraph executive summary
    of the global salary landscape.

    All key metrics are pre-calculated in Python and injected into the prompt.

    Parameters
    ----------
    overall_median : float
        The global median salary across all predictions.
    top_role : str
        The job category with the highest median salary.
    top_role_median : float
        The median salary for that top role.
    high_tier_median : float
        The median salary for High_Tier location countries.
    low_tier_median : float
        The median salary for Low_Tier location countries.
    total_records : int
        The total number of precomputed predictions in the dataset.

    Returns
    -------
    str
        A 3-paragraph executive summary. Never raises — returns
        FALLBACK_SUMMARY on any error.
    """
    geo_gap = high_tier_median - low_tier_median
    geo_gap_pct = (geo_gap / low_tier_median * 100) if low_tier_median else 0

    user_message = (
        f"Dataset Size: {total_records:,} precomputed salary predictions\n"
        f"Global Median Salary: ${overall_median:,.0f} USD\n"
        f"Highest Paying Role: {top_role} (Median: ${top_role_median:,.0f} USD)\n"
        f"High-Tier Location Median: ${high_tier_median:,.0f} USD\n"
        f"Low-Tier Location Median: ${low_tier_median:,.0f} USD\n"
        f"Geographic Wage Gap: ${geo_gap:,.0f} ({geo_gap_pct:.1f}% premium for high-tier)\n"
    )

    return _call_ollama(GLOBAL_SYSTEM_PROMPT, user_message, "global_summary", FALLBACK_SUMMARY)


